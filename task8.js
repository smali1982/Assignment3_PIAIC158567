console.log("Counting: ");
for (var i = 1; i <= 15; i++)
{console.log(i+ ", ");}

console.log("Reverse Counting: ");
for (var i = 10; i >= 1; i--)
{console.log(i+ ", ");}

console.log("Even: ");
for (var i = 0; i <= 20; i=i+2)
{console.log(i+ ", ");}

console.log("Odd: ");
for (var i = 1; i <= 19; i=i+2)
{console.log(i+ ", ");}

